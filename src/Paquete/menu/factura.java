            PreparedStatement lista = conexion.prepareStatement("SELECT * FROM clientes where nomnre_cli like'" + pesquisanome.getText() + "%' ORDER BY nomnre_cli");
            MODELO.removeAllElements();
            int v = 0;
            ResultSet resultado = lista.executeQuery();
            while(resultado.next() & v < 4){
                MODELO.addElement(resultado.getString("nomnre_cli"));
                v++;
            }
            if(v >=1){
                Lista.setVisible(true);
            }else{
                Lista.setVisible(false);
            }
