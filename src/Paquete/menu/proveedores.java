    private void listar(){
        Connection conexion;
        conexion = Conexion.getConnection();
        DefaultTableModel modelo = (DefaultTableModel) tablaProvee.getModel();
        modelo.setRowCount(0);
        try {
            PreparedStatement listar = conexion.prepareStatement("select * from proveedores");
            ResultSet resultado = listar.executeQuery();
            while (resultado.next()) {
                modelo.addRow(new Object[]{resultado.getInt("id_proveedores"), resultado.getString("nombre_prov"), resultado.getString("ruc_prov")});
            }

        } catch (Exception ex) {
            ex.printStackTrace();
        }
      }
    
    
