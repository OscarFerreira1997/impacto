    private void tablaProveeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaProveeMouseClicked
        Connection conexion;
        conexion = Conexion.getConnection();

        String nombr = String.valueOf(tablaProvee.getValueAt(tablaProvee.getSelectedRow(), 1).toString());
        try {
            PreparedStatement listar = conexion.prepareStatement("select * from proveedores where nombre_prov=?");
            listar.setString(1, nombr);
            ResultSet resultado = listar.executeQuery();
            while (resultado.next()) {
                jnombre.setText(resultado.getString("nombre_prov"));
                jtelefono.setText(resultado.getString("telefono_prov"));
                jruc.setText(resultado.getString("ruc_prov"));
                jcorreo.setText(resultado.getString("correo_prov"));
            }

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_tablaProveeMouseClicked

